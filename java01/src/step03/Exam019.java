public class Exam019 {
  public static void main(String[] args) {
    // v1 변수 선언(메모리 준비)
    int v1;

    // => assignment operator(배정 연산자, 할당 연산자)
    v1 = 20;
    // 배정 연산자 왼쪽 l-value 오른쪽을 r-value라 부른다.
    // => l-value = r-value;
    // l-value는 반드시 변수명(메모리)여야 한다.

    System.out.println(v1);
    
  }
}
