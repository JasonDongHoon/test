public class Exam022 {
  public static void main(String[] args) {
    double v1 = 3.14;
    double v2 = .14;
    double v3 = 3.;

    System.out.printf("v1 = %f\n", v1);
    System.out.printf("v2 = %f\n", v2);
    System.out.printf("v3 = %f\n", v3);

    /* 자바는 C와 달리 lf로 표기하지 않고 f로써 실수를 표현하는데
       자동으로 소수점 6자리 까지 표기한다. 또한 %.e3와 같이 표시할 소수점을
       지정할 수 없는듯 하다. 질문해야겠다.*/
  }
}
