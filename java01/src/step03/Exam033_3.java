// 주제: 변수 선언 - 배열의 크기 알아내기
package step03;

public class Exam033_3 {
  public static void main(String[] args) {
    //1) 배열에 값 저장하기
    int[] scores = new int[] {100, 100, 100};

    //2) 배열의 개수를 알아내기
    // => 모든 배열은 length라는 이름의 변수가 있다.
    //    이 변수는 배열 메모리의 개수를 보관한다.
    System.out.println(scores.length);
  }
}

