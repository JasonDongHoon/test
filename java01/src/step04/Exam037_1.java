// 주제: 연산자 - 전위(pre)/후위(post) 연산자
package step04;

public class Exam037_1 {
  public static void main(String[] args) {
    //1) 후위 연산자 사용전
    int a = 10;
    a = a + 1;
    System.out.println(a);

    //2) 후위 연산자 사용후
    int b = 10;
    b++;
    System.out.println(b);

    int c = 10;
    c--;
    System.out.println(c);
  }
}

