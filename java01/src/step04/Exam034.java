package step04;
//주제: 연산자 - 산술 연산자

public class Exam034 {
  public static void main(String[] args) {
    System.out.printf("10 + 8 = %d\n", 10 + 8);
    System.out.printf("10 - 8 = %d\n", 10 - 8);
    System.out.printf("10 * 8 = %d\n", 10 * 8);
    System.out.printf("10 / 8 = %d\n", 10 / 8); //몫만 출력
    System.out.printf("10 %% 8 = %d\n", 10 % 8); //나머지만 출력
  }
}

