// 주제: 연산자 - 산술연산자(+,-,*,/,%)
package step04;

public class Exam036_0 {
  public static void main(String[] args) {
    int a = 10, b = 7;
    int result = a + b;
    System.out.println(result);

    result = a - b;
    System.out.println(result);

    result = a * b;
    System.out.println(result);

    result = a / b;
    System.out.println(result);

    result = a % b;
    System.out.println(result);

  }
}

