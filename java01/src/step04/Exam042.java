package step04;
//주제: 연산자 - 관계 연산자 사용법

public class Exam042 {
  public static void main(String[] args) {
    // 조건을 따질 때 많이 사용한다.
    System.out.printf("10 == 20 ? %b\n", 10 == 20); //F
    System.out.printf("10 != 20 ? %b\n", 10 != 20); //T
    System.out.printf("10 < 20 ? %b\n", 10 < 20); //T
    System.out.printf("10 <= 20 ? %b\n", 10 <= 20); // ????? 하나만 맞다
    System.out.printf("10 > 20 ? %b\n", 10 > 20); // F
    System.out.printf("10 >= 20 ? %b\n", 10 >= 20); //F
  }
}

