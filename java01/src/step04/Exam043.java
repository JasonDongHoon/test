package step04;

public class Exam043 {
  public static void main(String[] args) {
    System.out.printf("T && T ? %b\n", true && true);
    System.out.printf("T && F ? %b\n", true && false);
    System.out.printf("F && T ? %b\n", false && true);
    System.out.printf("F && F ? %b\n", false && false);
    System.out.printf("T || T ? %b\n", true || true);
    System.out.printf("T || F ? %b\n", true || false);
    System.out.printf("F || T ? %b\n", false || true);
    System.out.printf("F || F ? %b\n", false || false);
  }
}
