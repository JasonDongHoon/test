// 주제: 자바에서 제공하는 명령어 사용해보기
public class Exam010 {
  public static void main(String[] args) {
    // Math 클래스에 정의되어 있는 abs() 메서드는 절대 값을 되돌려 준다.
    java.lang.Math.abs(-100); // 실행한 결과 값을 받지 않으면 아무 소용이 없다.
    int value = java.lang.Math.abs(-200);
    System.out.print(value + "\n");
  }
}

