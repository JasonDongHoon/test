// 주제: 자바에서 값을 표현하는 방법 - Literal
package step02;

public class Exam019 {
  public static void main(String[] args) {
    System.out.println("문자열");
    System.out.println((char)44032);
    System.out.println(10);
    System.out.println(3.14);
    System.out.println(true);
  }
}

