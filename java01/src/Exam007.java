// 주제: method 실행하기
public class Exam007 {
  public static void main(String[] args) {
    m1(); // 메서드 실행하기
  }

  static void m1() {
    System.out.print("m1\n");
    System.out.print("안녕하세요.\n");
  }
}

/*
=> 메서드 실행 문법
  method_name(argument, ...);

1) method_name
  - 실행할 메서드의 이름
2) argument
  - 실행할 메서드에 넘겨주는 값.
  - 콤마(,)를 사용하여 여러 개의 값을 넘겨 줄 수 있다.
  - 단, 그 메서드가 값을 받을 변수가 있을 때.




*/
