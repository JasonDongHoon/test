public class Exam014 {

  public static void main(String[] args) {
    boolean v = 10 == 20;
    System.out.println(v);

    v = 10 != 20;
    System.out.println(v);

    v = 10 < 20;
    System.out.println(v);

    v = 10 <= 20;
    System.out.println(v);

    v = 10 > 20;
    System.out.println(v);

    v = 10 >= 20;
    System.out.println(v);

    System.out.println(3.14f > 3.12f);
    System.out.println(3.14 > 3.140000000001);
    System.out.println('가' > '각');

  }
}
